import { createHash } from 'node:crypto';
import { cp, mkdir, readFile, readdir, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';
import { glob } from 'glob';
import { parseMarkdownLesson, type LessonFrontmatter } from './markdownLessonTransforms';
import { replaceMermaidWithCdnImages, replaceAssetsWithCdnUrls, injectPreworkLinks, PREWORK_BASE_URL } from './lessonHtmlPostProcessing';

export type LessonHtmlCourseKey =
  | 'lessons10xDevs3Pl'
  | 'lessons10xDevs3PreworkPl';

export interface LessonHtmlGenerationTarget {
  courseKey: LessonHtmlCourseKey;
  sourceGlob: string;
  outputDir: string;
  defaultLanguage?: 'pl' | 'en';
  requireLanguage: boolean;
  requireOrder: boolean;
  rootDir?: string;
  assetsPublicPath?: string;
}

export interface GeneratedLessonHtml {
  sourcePath: string;
  outputPath: string;
  html: string;
  lessonId: string;
  title: string;
  language?: 'pl' | 'en';
  order?: number;
}

export interface LessonHtmlCheckResult {
  ok: boolean;
  generated: GeneratedLessonHtml[];
  diagnostics: string[];
}

const GENERATED_MARKER = 'generated by generate-lesson-html';

function normalizePathForMetadata(filePath: string, rootDir: string): string {
  const relativePath = path.relative(rootDir, filePath);
  return relativePath.split(path.sep).join('/');
}

function escapeHtmlAttribute(value: string): string {
  return value
    .replace(/&/g, '&amp;')
    .replace(/"/g, '&quot;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

function getSourceHash(source: string): string {
  return createHash('sha256').update(source).digest('hex');
}

function isValidLanguage(language: unknown): language is 'pl' | 'en' {
  return language === 'pl' || language === 'en';
}

function validateLessonMetadata(
  sourcePath: string,
  frontmatter: LessonFrontmatter,
  target: LessonHtmlGenerationTarget
): {
  lessonId: string;
  language?: 'pl' | 'en';
  order?: number;
} {
  const lessonId = frontmatter.lessonId?.trim();
  if (!lessonId) {
    throw new Error(`Missing required lessonId in Markdown lesson: ${sourcePath}`);
  }

  const language = frontmatter.language ?? target.defaultLanguage;
  if (target.requireLanguage && !language) {
    throw new Error(`Missing required language in Markdown lesson: ${sourcePath}`);
  }

  if (language && !isValidLanguage(language)) {
    throw new Error(`Invalid language "${language}" in Markdown lesson: ${sourcePath}`);
  }

  if (target.requireOrder && (typeof frontmatter.order !== 'number' || !Number.isFinite(frontmatter.order))) {
    throw new Error(`Missing required numeric order in Markdown lesson: ${sourcePath}`);
  }

  return {
    lessonId,
    ...(language ? { language } : {}),
    ...(typeof frontmatter.order === 'number' ? { order: frontmatter.order } : {}),
  };
}

function getOutputPath(sourcePath: string, outputDir: string): string {
  const fileName = path.basename(sourcePath).replace(/\.md$/i, '.html');
  return path.join(outputDir, fileName);
}

