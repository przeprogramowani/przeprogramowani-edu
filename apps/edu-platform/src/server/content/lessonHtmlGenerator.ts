export function renderGeneratedLessonHtml({
  bodyHtml,
  courseKey,
  language,
  lessonId,
  order,
  sourceHash,
  sourcePath,
  title,
  titleEn,
}: {
  bodyHtml: string;
  courseKey: LessonHtmlCourseKey;
  language?: 'pl' | 'en';
  lessonId: string;
  order?: number;
  sourceHash: string;
  sourcePath: string;
  title: string;
  titleEn?: string;
}): string {
  const metaTags = [
    `<meta name="lesson-id" content="${escapeHtmlAttribute(title)}">`,
    titleEn ? `<meta name="title-en" content="${escapeHtmlAttribute(titleEn)}">` : null,
    `<meta name="source-path" content="${escapeHtmlAttribute(sourcePath)}">`,
    `<meta name="source-hash" content="${escapeHtmlAttribute(sourceHash)}">`,
    `<meta name="course-key" content="${escapeHtmlAttribute(courseKey)}">`,
    `<meta name="source" content="generated-html">`,
    `<meta name="canonical-lesson-id" content="${escapeHtmlAttribute(lessonId)}">`,
    language ? `<meta name="language" content="${escapeHtmlAttribute(language)}">` : null,
    typeof order === 'number' ? `<meta name="order" content="${order}">` : null,
  ].filter((tag): tag is string => tag !== null);

  return [
    `<!-- ${GENERATED_MARKER}; source: ${escapeHtmlAttribute(sourcePath)}; hash: ${escapeHtmlAttribute(sourceHash)} -->`,
    ...metaTags,
    '<html><head></head><body>',
    bodyHtml,
    '</body></html>',
    '',
  ].join('\n');
}

export async function generateLessonHtmlForSource(
  sourcePath: string,
  source: string,
  target: LessonHtmlGenerationTarget
): Promise<GeneratedLessonHtml | null> {
  const rootDir = target.rootDir ?? process.cwd();
  const parsedLesson = await parseMarkdownLesson(sourcePath, source);

  if (parsedLesson.frontmatter.hidden) {
    return null;
  }

  const metadata = validateLessonMetadata(sourcePath, parsedLesson.frontmatter, target);
  const relativeSourcePath = normalizePathForMetadata(sourcePath, rootDir);
  const sourceHash = getSourceHash(source);

  let bodyHtml = parsedLesson.bodyHtml;
  if (target.assetsPublicPath) {
    bodyHtml = bodyHtml.replace(/src="\.\/assets\//g, `src="${target.assetsPublicPath}/`);
  }
  bodyHtml = replaceAssetsWithCdnUrls(bodyHtml);
  bodyHtml = replaceMermaidWithCdnImages(bodyHtml);
  bodyHtml = injectPreworkLinks(bodyHtml, target.defaultLanguage ?? 'pl', PREWORK_BASE_URL);

  const html = renderGeneratedLessonHtml({
    bodyHtml,
    courseKey: target.courseKey,
    language: metadata.language,
    lessonId: metadata.lessonId,
    order: metadata.order,
    sourceHash,
    sourcePath: relativeSourcePath,
    title: parsedLesson.title,
    titleEn: parsedLesson.frontmatter.titleEn,
  });

  return {
    sourcePath,
    outputPath: getOutputPath(sourcePath, target.outputDir),
    html,
    lessonId: metadata.lessonId,
    title: parsedLesson.title,
    ...(metadata.language ? { language: metadata.language } : {}),
    ...(typeof metadata.order === 'number' ? { order: metadata.order } : {}),
  };
}

export async function generateLessonHtmlForTargets(
  targets: LessonHtmlGenerationTarget[]
): Promise<GeneratedLessonHtml[]> {
  const generated: GeneratedLessonHtml[] = [];

  for (const target of targets) {
    const sourceFiles = await glob(target.sourceGlob, { nodir: true });

    for (const sourcePath of sourceFiles.sort((a, b) => a.localeCompare(b, undefined, { numeric: true }))) {
      const source = await readFile(sourcePath, 'utf-8');
      const generatedLesson = await generateLessonHtmlForSource(sourcePath, source, target);

      if (generatedLesson) {
        generated.push(generatedLesson);
      }
    }
  }

  assertUniqueOutputPaths(generated);

  return generated.sort((a, b) => a.outputPath.localeCompare(b.outputPath, undefined, { numeric: true }));
}

function assertUniqueOutputPaths(generated: GeneratedLessonHtml[]): void {
  const seen = new Map<string, string>();

  for (const lesson of generated) {
    const previousSource = seen.get(lesson.outputPath);
    if (previousSource) {
      throw new Error(
        `Duplicate generated lesson output "${lesson.outputPath}" from "${previousSource}" and "${lesson.sourcePath}"`
      );
    }

    seen.set(lesson.outputPath, lesson.sourcePath);
  }
}

async function listGeneratedLookingHtmlFiles(dir: string): Promise<string[]> {
  let entries: string[];

  try {
    entries = await readdir(dir);
  } catch (error) {
    if ((error as NodeJS.ErrnoException).code === 'ENOENT') {
      return [];
    }

    throw error;
  }

  const generatedFiles: string[] = [];
  for (const entry of entries) {
    if (!entry.endsWith('.html')) {
      continue;
    }

    const filePath = path.join(dir, entry);
    const content = await readFile(filePath, 'utf-8');
    if (content.includes(GENERATED_MARKER)) {
      generatedFiles.push(filePath);
    }
  }

  return generatedFiles;
}

export async function writeGeneratedLessonHtml(
  targets: LessonHtmlGenerationTarget[]
): Promise<GeneratedLessonHtml[]> {
  const generated = await generateLessonHtmlForTargets(targets);

  for (const lesson of generated) {
    await mkdir(path.dirname(lesson.outputPath), { recursive: true });
    await writeFile(lesson.outputPath, lesson.html, 'utf-8');
  }

  const rootDir = targets[0]?.rootDir ?? process.cwd();
  for (const target of targets) {
    if (!target.assetsPublicPath) continue;

    const contentAssetsDir = path.join(target.outputDir, 'assets');
    if (!existsSync(contentAssetsDir)) continue;

    const publicAssetsDir = path.join(rootDir, 'public', target.assetsPublicPath);
    await cp(contentAssetsDir, publicAssetsDir, { recursive: true });
  }

  return generated;
}

export async function checkGeneratedLessonHtml(
  targets: LessonHtmlGenerationTarget[]
): Promise<LessonHtmlCheckResult> {
  const generated = await generateLessonHtmlForTargets(targets);
  const diagnostics: string[] = [];
  const expectedPaths = new Set(generated.map((lesson) => lesson.outputPath));

  for (const lesson of generated) {
    let existing: string;

    try {
      existing = await readFile(lesson.outputPath, 'utf-8');
    } catch (error) {
      if ((error as NodeJS.ErrnoException).code === 'ENOENT') {
        diagnostics.push(`Missing generated HTML: ${lesson.outputPath}`);
        continue;
      }

      throw error;
    }

    if (existing !== lesson.html) {
      diagnostics.push(`Stale generated HTML: ${lesson.outputPath}`);
    }
  }

  for (const target of targets) {
    const generatedLookingFiles = await listGeneratedLookingHtmlFiles(target.outputDir);
    for (const filePath of generatedLookingFiles) {
      if (!expectedPaths.has(filePath)) {
        diagnostics.push(`Extra generated HTML without source: ${filePath}`);
      }
    }
  }

  return {
    ok: diagnostics.length === 0,
    generated,
    diagnostics,
  };
}

